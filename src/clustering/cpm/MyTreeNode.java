package clustering.cpm;

import javax.swing.tree.DefaultMutableTreeNode;
/**
 * render the tree nodes
 * @author Dequan
 *
 */
public class MyTreeNode extends DefaultMutableTreeNode
{

    public MyTreeNode()
    {
        flag = false;
    }

    public MyTreeNode(Object o)
    {
        super(o);
        flag = false;
    }

    public MyTreeNode(Object o, boolean flag)
    {
        super(o);
        this.flag = flag;
    }

    public boolean getFlag()
    {
        return flag;
    }

    boolean flag;
}
