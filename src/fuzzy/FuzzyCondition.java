package fuzzy;

public class FuzzyCondition {
	
	public static final int EQUAL = 1;
	public static final int NOTEQUAL = 2;
	public static final int GT = 3;
	public static final int LT = 4;
	public static final int GTE = 5;
	public static final int LTE = 6;
	
	private String fuzzyTerm;
	private int operator;

	public FuzzyCondition(String fuzzyTerm, int operator) {
		super();
		this.fuzzyTerm = fuzzyTerm;
		this.operator = operator;
	}
	public String getFuzzyTerm() {
		return fuzzyTerm;
	}

	public void setFuzzyTerm(String fuzzyTerm) {
		this.fuzzyTerm = fuzzyTerm;
	}

	public int getOperator() {
		return operator;
	}

	public void setOperator(int operator) {
		this.operator = operator;
	}
}
